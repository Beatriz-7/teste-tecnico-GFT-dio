package exercicio3.possivelDoenca;


import java.util.Scanner;

public class Doenca {
    public static void main(String[] args) {

        System.out.println("Informe os sintomas: ");
        Scanner in = new Scanner(System.in);

        int Sintomas = in.nextInt();
        int possíveisDoenças = 0;
        Sintomas = Sintomas + possíveisDoenças;

        System.out.println("As possíveis doenças são" + possíveisDoenças);

    }
}

